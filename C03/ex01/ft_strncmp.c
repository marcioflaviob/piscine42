#include <unistd.h>
#include <stdio.h>
#include <string.h>

int	ft_strcount(char *str, char *str2, unsigned int n)
{
	int	i;
	int	int_n;
	int	size;

	i = 0;
	int_n = (int) n;
	size = 0;
	while (str[i] == str2[i] && str[i] != '\0' && i < int_n)
		i++;
	if ((int_n != i) && (str[i] != str2[i]))
		size = ((int) str[i]) - ((int) str2[i]);
	return (size);
}

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	size;

	size = ft_strcount(s1, s2, n);
	return (size);
}
